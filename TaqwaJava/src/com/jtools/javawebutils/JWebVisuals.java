  public static Color getNext(int index, int template) {
    Color[] clrs = null;
    if (template == TEMPLATE_DEFAULT) {
      clrs = defaultTemplate;
    } else if (template == TEMPLATE_ALERTS) {
      clrs = alerts;
    } else if (template == TEMPLATE_EXCEL) {
      clrs = excel;
    } else if (template == TEMPLATE_PIE) {
      clrs = pie;
    } else if (template == TEMPLATE_MEDIA) {
      clrs = media;
    }
    if (clrs != null) {
      if (index >= clrs.length)
        index = index % clrs.length;
      return clrs[index];
    }
    return new Color((int)(250 * Math.random()), (int)(250 * Math.random()), (int)(250 * Math.random()));
  }

